                                        <div class="blog-meta">
                                            <h3>Musik Upbeat Dapat Membuat Latihan Sulit Lebih Mudah Dilakukan</h3>
                                            <ul class="list-inline">
                                                <li>20 Sept 2021</li>
                                                <li>
                                                    <span>Sumber :</span> 
                                                    <a href="https://www.healthline.com" target="blank">healthline.com</a>
                                                </li>
                                            </ul>
                                        </div>

                                        <div class="image-wrap">
                                            <img src="images/article/20092021/1.jpg" alt="" class="img-responsive" style="border-radius: 10px;">
                                        </div>

                                        <div class="blog-desc-big">
                                            <br>
                                            <p>
                                                Para peneliti mengatakan orang-orang lebih menikmati latihan HIIT ketika musik bertempo cepat dimainkan.
                                            </p>
                                            <p>
                                                Pelatihan interval intensitas tinggi (HIIT) — yang ditandai dengan sesi latihan intens yang singkat dan berulang yang dipisahkan oleh periode istirahat — dapat menjadi tantangan bagi siapa saja. Sangat sulit untuk memulai latihan HIIT jika Anda relatif tidak aktif.
                                            </p>    
                                            <p>
                                                “Meskipun HIIT efisien waktu dan dapat memperoleh manfaat kesehatan yang berarti di antara orang dewasa yang kurang aktif, satu kelemahan utama adalah bahwa orang mungkin menganggapnya tidak menyenangkan,” kata Matthew Stork, seorang rekan postdoctoral di School of Health and Exercise Sciences di Universitas British Columbia Kanada, Okanagan. “Akibatnya, ini berpotensi menghambat partisipasi yang berkelanjutan.”
                                            </p>
                                            <p>
                                                Jawabannya adalah musik.
                                            </p>    
                                            <p>
                                                Penelitian yang dipimpin oleh Stork dan diterbitkan dalam jurnal Psychology of Sport and Exercise menunjukkan bahwa musik yang ceria dapat membuat latihan HIIT tampak lebih mudah.
                                            </p>
                                            <p>
                                                Bahkan mungkin memotivasi orang yang tidak aktif untuk mulai berolahraga.
                                            </p>
                                            <p>
                                               Stork bekerja dengan peneliti Brunel University London Costas Karageorghis, seorang ahli musik dan olahraga, untuk mengumpulkan panel ahli untuk menilai 16 lagu bertempo tinggi dan memilih tiga yang mereka anggap paling memotivasi.
                                            </p>
                                            <p>
                                               Lagu-lagu yang dipilih adalah "Let's Go" oleh Calvin Harris (featuring Ne-Yo), "Bleed It Out" oleh Linkin Park, dan "Can't Hold Us" oleh Macklemore dan Ryan Lewis. Semua memiliki tempo yang lebih tinggi dari rata-rata lebih dari 135 denyut per menit (bpm).
                                            </p>
                                            <p>
                                               Para peneliti kemudian menempatkan sekelompok 24 peserta melalui latihan HIIT singkat - tiga sprint 20 detik yang dipisahkan oleh waktu istirahat singkat selama total 10 menit (termasuk periode pemanasan dan pendinginan).
                                            </p>
                                            <p>
                                                Kinerja latihan dinilai dengan musik motivasi serta dengan pemutaran podcast non-musik dan sesi tanpa audio.
                                            </p>
                                            <p>
                                                Peserta melaporkan sendiri kenikmatan HIIT yang lebih besar saat musik diputar. Detak jantung dan kinerja puncak mereka selama latihan juga meningkat seiring dengan irama.
                                            </p>
                                            <p>
                                                "Kami percaya bahwa musik motivasi akan membantu orang lebih menikmati latihan, tetapi kami terkejut dengan peningkatan detak jantung," kata Stork.
                                            </p>
                                            <p>
                                                Dia mencatat bahwa temuan tersebut mencerminkan fenomena yang dikenal yang disebut "entrainment," yang merupakan kecenderungan manusia untuk mengubah ritme biologis mereka ke irama musik.
                                            </p>
                                            <p class="lead">
                                                Lirik adalah hal penting
                                            </p>
                                            <p>
                                                Selain tempo dan ketukan dari tiga lagu yang dipilih, peneliti percaya bahwa sifat motivasi dari lirik juga dapat berperan dalam meningkatkan dan mempertahankan latihan. "Let's Go," misalnya, mendorong pendengar untuk "tidak membuat alasan sekarang," sementara "Get Up" mendesak, "Mencari cara yang lebih baik untuk bangun dari tempat tidur, Daripada mencari di internet dan memeriksa siapa yang membangunkan saya."
                                            </p>
                                            <p>
                                                "Lirik sangat penting dalam hal potensi motivasi mereka," Joe Bennett, PhD, seorang ahli musik di Berklee College of Music di Boston, mengatakan kepada Healthline. Bennett juga mencatat bahwa lagu-lagu yang digunakan dalam penelitian ini memiliki karakteristik lain seperti ketukan “four on the floor” yang kuat dan drop dinamis yang membangun suasana– keduanya umum dalam musik dansa berenergi tinggi.
                                            </p>
                                            <p>
                                                Penelitian tentang latihan intensitas rendah seperti joging telah menemukan bahwa musik memiliki efek disosiatif — musik mengalihkan perhatian Anda dari rasa sakit atau ketidaknyamanan yang dialami selama latihan
                                            </p>
                                            <p>
                                                "Rasanya seperti Anda tidak berolahraga sebanyak itu," kata Stork.
                                            </p>
                                            <p>
                                                “Musik meningkatkan upaya Anda, mendorong Anda untuk melampaui batas, dan menentukan pemeliharaan kecepatan dan/atau peningkatan kecepatan. Tubuh memiliki waktu yang lebih mudah dan lebih alami mengikuti ini daripada pikiran yang tenang atau suara napas sendiri, ”Sara Davis, seorang instruktur di CycleBar di Atlanta, Georgia, mengatakan kepada Healthline. Namun, dengan HIIT, “tampaknya musik paling efektif jika memiliki tempo yang cepat dan sangat memotivasi,” kata Stork
                                            </p>
                                            <p>
                                                “Saya telah secara konsisten menyaksikan bagaimana musik dapat mendorong orang untuk melakukan lebih dari yang mereka lakukan tanpa musik, terutama ketika musik diselaraskan dengan gerakan latihan,” kata Arien Reeberg, instruktur Zumba dan pelatih kebugaran.
                                            </p>
