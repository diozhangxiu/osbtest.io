                                            <br>
                                            <p>
                                                Ada banyak jenis penurunan berat badan di luar sana. Beberapa fokus pada pengurangan nafsu makan, 
                                                sementara yang lain membatasi kalori, karbohidrat, atau lemak. Karena semuanya mengklaim lebih unggul, 
                                                mungkin sulit untuk mengetahui mana yang pantas untuk dicoba.<br>
                                            </p>
                                            <p>
                                                Yang benar adalah bahwa tidak ada satu diet yang terbaik untuk semua orang - dan apa yang berhasil untuk 
                                                Anda mungkin tidak berhasil untuk orang lain. Berikut ini adalah 9 diet penurunan berat badan paling 
                                                populer beserta kelebihan dan kekurangannya. 
                                            </p>
                                            <p class="lead">
                                                1. Intermittent fasting
                                            </p>
                                            <p>
                                                Salah satu jenis diet yang terkenal dalam masyarakat adalah intermittent fasting. Konsep diet ini berpusat 
                                                pada siklus antara berpuasa dan makan. Diet ini tidak melarang mengonsumsi makan tertentu dan lebih ke 
                                                mengontrol jam makan.<br>
                                                Misalnya, Anda dapat mengonsumi makanan selama delapan jam dan 16 jam sisanya, Anda akan berpuasa dan 
                                                tidak boleh mengonsumsi makanan ataupun minuman yang berkalori. Diet ini dinilai sangat efektif dalam 
                                                menurunkan berat badan asal Anda tidak makan secara berlebih atau melewati kalori harian selama waktu 
                                                makan. Akan tetapi, intermittent fasting tidak terlalu efektif pada wanita.<br>
                                            </p>    
                                            <p>
                                                Seseorang dengan kondisi medis tertentu, seperti wanita yang sedang hamil dan menyusui, orang yang 
                                                malnutrisi, dan orang yang memiliki masalah dengan kadar gula darah, perlu menghindari diet jenis ini.<br>
                                                
                                            </p>
                                            <p class="lead">
                                                2. Diet vegan
                                            </p>
                                            <p>
                                                Diet vegan dikenal sebagai jenis diet yang tidak memperbolehkan adanya konsumsi produk olahan hewani sama 
                                                sekali, termasuk telur dan madu. Diet ini merupakan pola makan yang rendah lemak dan tinggi serat, 
                                                sehingga efektif untuk menurunkan berat badan.<br>
                                                Tidak hanya itu, diet vegan juga mengurangi risiko penyakit jantung, kanker, dan penyakit Alzheimer. Akan 
                                                tetapi, ada beberapa jenis produk hewan yang mengandung nutrisi tertentu yang tidak bisa dipenuhi melalui 
                                                diet vegan, seperti vitamin B-12, zat besi, vitamin D, kalsium, dan asam lemak omega-3.
                                                <br>
                                            </p>    
                                            <p>
                                                Seringkali ini menjadi kesalahan diet vegan yang dilakukan pemula, yaitu tidak mempertimbangkan nutrisi 
                                                yang tidak bisa didapatkan dari produk nabati. Tapi hal ini bisa diatasi dengan mengonsumsi multivitamin.
                                            </p>
                                            <br>
                                            <div class="image-wrap">
                                                <img src="images/article/12072021/2.jpg" alt="" class="img-responsive" style="border-radius: 10px;">
                                            </div>
                                            <br>
                                            <p class="lead">
                                                3. Diet vegetarian
                                            <p>
                                                Berbeda dengan diet vegan yang ketat, diet vegetarian masih memperbolehkan konsumsi telur, produk susu, 
                                                dan madu. Diet vegetarian juga dinilai ampuh dalam membantu penurunan berat badan.
                                            </p>
                                            <p>
                                               Namun, diet vegetarian juga berpotensi membuat kita kekurangan nutrisi tertentu, seperti protein dan kolin.
                                            </p>
                                            <p class="lead">
                                                4. Diet Mediterania
                                            <p>
                                                Diet Mediterania adalah jenis diet yang terinspirasi dari pola makan masyarakat Eropa Selatan. Diet 
                                                Mediterania berfokus pada konsumsi buah-buahan, sayur-mayur, biji-bijian, produk gandum utuh, ikan, ayam, 
                                                keju, yogurt, dan minyak zaitun.
                                            </p>
                                            <p>
                                               Kita masih bisa mengonsumsi setidaknya empat telur per minggunya, serta anggur merah (red wine) dan daging 
                                               merah dalam jumlah yang sedikit. Jenis diet ini membantu menurunkan berat badan dan risiko terkena penyakit. 
                                               Akan tetapi, beberapa bahan makanan pada diet Mediterania biasanya mahal. Selain itu, penderita diabetes 
                                               perlu berkonsultasi dengan dokter sebelum menjalani diet Mediterania.
                                            </p>
                                            <p class="lead">
                                                5. Diet paleo
                                            <p>
                                                Diet paleo merupakan salah satu jenis diet yang meyakini bahwa manusia perlu mengonsumsi makanan yang 
                                                dahulu dimakan oleh manusia purbakala. Beberapa makanan yang diperbolehkan adalah buah-buahan, sayur-mayur, 
                                                kacang-kacangan, biji-bijian, dan protein rendah lemak.
                                            </p>
                                            <p>
                                               Saat menjalani diet paleo, Anda tidak dianjurkan mengonsumsi gula, produk susu, produk gandum, dan makanan 
                                               yang diproses. Diet paleo ditemukan efektif dalam menurunkan berat badan dengan mengurangi konsumsi 
                                               karbohidrat harian. Selain itu, diet paleo juga mampu mengurangi kadar gula darah, tekanan darah, dan 
                                               kolesterol. Namun, kekurangan dari diet ini adalah berkurangnya asupan nutrisi dari produk susu dan produk 
                                               gandum, seperti keju dan roti.
                                            </p>
                                            <p class="lead">
                                                6. Diet rendah karbohidrat
                                            <p>
                                                Mengurangi konsumsi karbohidrat adalah moto dari penganut diet rendah karbohidrat. Pola makan ini membatasi 
                                                konsumsi karbohidrat harian menjadi hanya sebanyak 20-150 gram per harinya. Diet rendah karbohidrat membuat 
                                                tubuh untuk menggunakan energi lemak alih-alih dari karbohidrat.
                                            </p>
                                            <p>
                                               Jenis diet yang satu ini sangat berdampak pada penurunan berat badan, khususnya untuk orang-orang dengan 
                                               berat badan berlebih dan obesitas. Namun, diet rendah karbohidrat belum tentu cocok untuk semua orang, 
                                               karena tidak semua orang merasa nyaman menjalani pola makan ketat ini. Pada kasus tertentu, penganut diet 
                                               rendah karbohidrat malah mengalami peningkatan kadar kolesterol LDL jahat dan beberapa orang malah menderita 
                                               ketoasidosis non-diabetik yang muncul karena penumpukan asam dalam darah.
                                            </p>
                                            <br>
                                            <div class="image-wrap">
                                                <img src="images/article/12072021/3.jpg" alt="" class="img-responsive" style="border-radius: 10px;">
                                            </div>
                                            <br>
                                            <p class="lead">
                                               7. Diet ketogenic
                                            </p>
                                            <p>
                                                Mengurangi karbohidrat dan meningkatkan konsumsi lemak sehat, seperti alpukat dan ikan berlemak, merupakan 
                                                inti dari diet ketogenik atau yang akrab disebut sebagai diet keto. Diet ketogenik menurunkan berat badan 
                                                dengan membuat tubuh menggunakan lemak sebagai sumber energinya.

                                            </p>
                                            <p>
                                                Mirip dengan diet rendah karbohidrat, kita berpotensi mengalami ketoasidosis saat menjalani diet ini. 
                                                Konsultasikan ke dokter untuk menyesuaikan apakah diet ini sesuai untuk kondisi tubuh kita..
                                            </p>
                                            <p class="lead">
                                                8. Diet Dukan Diet
                                            </p>
                                            <p>
                                                Dukan merupakan jenis diet yang terbagi menjadi empat fase, yaitu dua fase penurunan berat badan dan empat 
                                                fase mempertahankan berat badan. Diet ini menekankan konsumsi makanan tinggi protein dan rendah 
                                                karbohidrat. Pada fase awal kita hanya akan mengonsumsi makanan tinggi protein dan dedak gandum (oat bran). 
                                                Pada fase-fase berikutnya barulah bisa menambahkan sayuran yang tidak mengandung pati, serta beberapa 
                                                karbohidrat dan lemak.
                                            </p>
                                            <p>
                                                Diet dukan cukup efektif dalam menurunkan berat badan, meningkatkan metabolisme tubuh, dan mengurangi 
                                                hormon ghrelin yang memicu rasa lapar. Hanya saja, masih diperlukan lebih banyak penelitian untuk 
                                                mengetahui efektivitas dari diet ini. Hal ini karena diet Dukan mengurangi asupan lemak dan karbohidrat 
                                                yang berpotensi mengurangi massa otot dan meningkatkan peluang kenaikan berat badan dengan cepat.
                                            </p>
                                            <p class="lead">
                                                9. Diet Atkins
                                            </p>
                                            <p>
                                                Hampir mirip dengan diet rendah karbohidrat, diet Atkins juga berpusat pada konsumsi makanan yang 
                                                mengandung protein dan lemak, serta menghindari makanan yang mengandung karbohidrat. Diet Atkins terbagi 
                                                menjadi empat fase. Pada fase pertama, kita hanya diperbolehkan mengonsumsi 20 gram karbohidrat per 
                                                harinya selama dua minggu.
                                            </p>
                                            <p>
                                                Pada fase-fase berikutnya, barulah dapat menambah karbohidrat secara perlahan. Diet Atkins mampu 
                                                menurunkan berat badan dan mengurangi lemak di perut, serta mengurangi risiko gangguan pada kolesterol, 
                                                nsulin, tekanan darah, dan gula darah. Namun, diet Atkins berpeluang memunculkan beberapa efek samping, 
                                                seperti lemas, sakit kepala, sembelit, dan dehidrasi. Diet ini juga merupakan jenis diet yang ketat dan 
                                                mungkin sulit untuk dijalani.
                                            </p>
                                            <br>
                                            <p>
                                                Ada berbagai jenis diet dengan beragam kelebihan dan kekurangan. Tidak ada satu jenis diet yang cocok 
                                                untuk semua orang. Anda perlu mencari tahu diet mana yang sesuai untukmu. Pilihlah diet yang bisa 
                                                dijadikan pola makan sehari-hari dan membuat nyaman untuk menjalani dalam jangka waktu panjang. Apabila 
                                                mengalami kesulitan untuk menentukan jenis diet mana yang sesuai, Anda dapat berkonsultasi dengan dokter 
                                                atau ahli gizi.
                                            </p>
                                            <p>
                                                Apabila Anda yang ingin mencari menu sehat untuk memulai program diet, bergizi dan bebas minyak tanpa 
