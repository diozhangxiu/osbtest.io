                                                angkat berat yang tiba-tiba selama berolahraga, atau saat melakukan tugas kerja.<br>
                                            </p>
                                            <p>
                                                Kerusakan otot dapat berupa robeknya (sebagian atau seluruh) serat otot dan tendon yang melekat pada otot. 
                                                Robeknya otot juga dapat merusak pembuluh darah kecil, menyebabkan pendarahan lokal, atau memar, dan 
                                                nyeri akibat iritasi pada ujung saraf di daerah tersebut.
                                            </p>
                                            <p class="lead">
                                                Gejala Cedera Otot
                                            </p>
                                            <p>
                                                Anda biasanya akan merasakan cedera otot saat itu terjadi. Gejalanya meliputi:<br>

                                                •	rasa sakit yang tiba-tiba<br>
                                                •	rentang gerak terbatas<br>
                                                •	memar atau perubahan warna<br>
                                                •	pembengkakan<br>
