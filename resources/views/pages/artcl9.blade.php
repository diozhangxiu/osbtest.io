                                                •	olahraga <br>
                                                •	menahan punggung atau leher Anda dalam posisi tidak tepat untuk waktu yang lama, seperti saat Anda 
                                                bekerja di meja<br>
                                                •	postur tubuh yang buruk <br>

                                            </p>
