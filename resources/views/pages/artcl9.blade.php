                                                •	kekakuan pada otot/sendi<br>
                                                •	kelemahan pada otot/sendi<br>
                                            </p>    
                                            <p>
                                                Dalam cedera ringan, otot yang robek mungkin terasa sedikit kaku, tetapi masih cukup fleksibel untuk 
                                                digunakan. Cedera otot yang parah adalah ketika otot robek parah. Hal ini menyebabkan rasa sakit dan 
                                                gerakan yang sangat terbatas.<br>
                                                Gejala cedera otot ringan hingga sedang biasanya hilang dalam beberapa minggu. Cedera yang lebih parah 
                                                mungkin membutuhkan waktu berbulan-bulan untuk sembuh.
                                            </p>
                                            <p class="lead">
                                                Penyebab cedera otot
                                            </p>
                                            <p>
                                                Cedera otot akut adalah ketika otot Anda robek secara tiba-tiba dan tidak terduga. Hal seperti itu 
                                                dapat terjadi baik dari cedera atau trauma. Hal ini dapat disebabkan oleh:<br>

                                                •	tidak melakukan pemanasan dengan benar sebelum melakukan aktivitas fisik<br>
                                                •	fleksibilitas yang buruk<br>
                                                •	pengkondisian yang buruk<br>
                                                •	kelelahan
                                                <br>
                                            </p>    
                                            <p>
                                                Ada kesalahpahaman bahwa hanya latihan keras dan latihan intensitas tinggi yang menyebabkan cedera 
                                                otot. cedera otot bahkan dapat terjadi karena berjalan.
                                            </p>
                                            <p>
                                                Cedera akut dapat terjadi ketika Anda:<br>

                                                •	tergelincir atau kehilangan pijakan Anda<br>
                                                •	melompat<br>
                                                •	lari<br>
                                                •	melempar sesuatu<br>
                                                •	angkat sesuatu yang berat<br>
                                                •	mengangkat sesuatu saat Anda berada dalam posisi yang tidak tepat<br>

                                            </p>
                                            <p>
                                                Cedera otot akut juga lebih sering terjadi pada cuaca dingin. Ini karena otot lebih kaku pada suhu 
                                                yang lebih rendah. Penting untuk meluangkan waktu ekstra untuk melakukan pemanasan dalam kondisi ini untuk 
                                                mencegah cedera.
