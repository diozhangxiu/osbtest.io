                                        <div class="blog-desc-big">
                                            <p>
                                                Saat ini, fitnes bukanlah sebuah hal yang menakutkan lagi bagi wanita. Seiring berkembangnya zaman, fitnes tidak lagi hanya berurusan dengan membentuk otot menjadi kekar, tapi juga bisa digunakan sebagai metode kebugaran dan menurunkan berat badan. Maka dari itu, tidak sedikit wanita zaman now yang mengikuti program fitnes di berbagai tempat kebugaran. Lebih dari itu, para wanita juga tidak takut jika tubuhnya akan menjadi kekar layaknya binararaga. Sebab, anggapan itu hanyalah mitos belaka yang tidak benar. Karena pada dasarnya ada beberapa teknik fitnes yang bisa dilakukan sesuai dengan kebutuhan. Jika menginginkan tubuh yang kekar dan berotot, tentu saja harus melakukan latihan intens pada alat beban. Namun, ketika ingin mengecilkan perut atau menurunkan berat badan, tentu saja latihannya berbeda.
                                            </p>
                                            <p>
                                                Selain itu, pola makan juga sangat berbeda. Ketika ingin membentuk massa otot, setiap pelaku fitnes dianjurkan untuk lebih banyak makanan, terutama makanan yang kaya akan protein, lemak, dan karbohidrat. Tapi, saat ingin menurunkan berat badan, justru setiap orang dilarang mengonsumsi makanan berlebihan. Sehingga, tidak perlu ada rasa takut lagi ketika ingin melakukan latihan fitnes di pusat kebugaran.
                                            </p>
                                            <p>
                                                Nah, biasanya seorang pemula dalam dunia fitnes akan merasakan bingung harus mulai dari mana, apa yang harus dilakukan, serta teknik apa saja yang perlu dikuasai. Hal itu adalah wajar, sebab bagaimanapun juga pemula membutuhkan penyesuaian terlebih dahulu beberapa saat sampai akhirnya terbiasa.
                                            </p>
                                            <p>
                                                Namun, buat kamu yang ingin bergabung dalam dunia kebugaran, tentu saja kamu harus mengetahui terlebih dahulu apa saja tips-tips sebelum melakukan fitnes. Hal ini bertujuan agar latihanmu menjadi lebih maksimal dan tidak membutuhkan waktu lama untuk penyesuaian.
                                            </p>
                                            <p>
                                                Daripada penasaran, berikut ini adalah beberapa tips fitnes bagi pemula.
                                            </p>
                                            <p class="lead">
                                                1. Punya Target
                                            </p>
                                            <p>
                                                Sebelum bergabung ke dunia kebugaran, kamu harus tahu terlebih dahulu apa targetmu. Seperti yang sudah dijelaskan di atas, target itu penting agar kamu tidak sampai salah langkah dalam melakukan latihan. Misalnya kamu ingin menurunkan berat badan, maka yang harus dilakukan adalah melakukan latihan kebugaran, bukan fokus di angkat beban.
                                            </p>
                                            <p class="lead">
                                                2. Ciptakan Motivasi
                                            </p>
                                            <p>
                                                Selanjutnya, kamu harus punya motivasi dalam melakukannya. Sebab, apapun yang dilakukan tanpa motivasi, pasti tidak akan semangat. Untuk itu, kamu harus punya motivasi tersendiri agar latihn yang kamu lakukan menjadi lebih asik dan menyenangkan. Misalnya motivasimu adalah ingin kurus agar lebih disayang pacar.
                                            </p>
                                            <p class="lead">
                                                3. Cari Pembimbing
                                            </p>
                                            <p>
                                                Sebagai pemula, tentu saja kamu tidaak memiliki bekal pengetahuan yang cukup tentang dunia fitnes, bukan? Dengan begitu, kamu harus menggali informasi seputar fitnes kepada orang yang berpengalaman. Pembimbing tidak harus personal trainer, tapi seseorang atau teman yang lebih berpengalaman daripada kamu. Nantinya dia akan berbagai pengalaman serta tipsnya saat melakukan fitnes.
                                            </p>
                                            <p class="lead">
                                                4. Pakai Kostum yang Cocok
                                            </p>
                                            <p>
                                                Hal ini merupakan hal penting yang sering disepelekan oleh pemula. Ketika ingin melakukan fitnes, tentu saja kamu harus menggunakan kostum olahraga agar proses latihan menjadi lebih menyenangkan dan nyaman. Selain itu, menggunakan kostum yang salah akan membuat gerakmu menjadi terbatas, akhirnya latihan jadi tidak maksimal.
                                            </p>
                                            <p class="lead">
                                                5. Ukur Kemampuanmu
                                            </p>
                                            <p>
                                                Tidak sedikit pemula yang sangat tinggi obsesinya saat mengikuti fitnes. Mereka akan sangat semangat dan menggebu untuk cepat mendapat hasil. Padahal, sebaiknya latihan itu harus dilakukan sesuai dengan kemampuan diri. Sebab, saat kamu memaksakan tubuh, bukan hasil baik yang kamu dapatkan, melainkan hasil buruk, seperti cedera otot.
                                            </p>
                                            <p class="lead">
                                                6. Jangan Latihan Monoton
                                            </p>
                                            <p>
                                                Saat melakukan latihan, usahakan untuk tidak monoton. Artinya, kamu harus mencoba beberapa teknik dalam satu sesi latihan. Sebab, ketika kamu melakukan latihan yang sama dalam waktu lama, maka kamu akan merasa bosan dan hasilnya tidak merata pada semua bagian tubuh.
                                            </p>
                                            <p class="lead">
                                                7. Jaga Asupan Makanan
                                            </p>
                                            <p>
                                                Yang tidak kalah penting juga kamu harus menjaga pola makan yang baik dan benar. Tentu saja asupan makanan ini berkaitan dengan tujuan atau target. Namun, secara keseluruhan, pelaku fitnes dianjurkan untuk mengonsumsi makanan sehat yang banyak serat serta mengonsumsi air putih. Hal itu dikarenakan banyak energi dan cairan yang keluar dari tubuh saat melakukan fitnes.
                                            </p>
                                            <p class="lead">
                                                8. Perhatikan Jadwal Latihan
                                            </p>
                                            <p>
                                                Yang terakhir dan harus diperhatikan adalah mengatur jadwal latihan yang benar dan tepat. Sebagai pemula, kamu tidak dianjurkan untuk latihan setiap hari. Tapi kamu bisa melakukannya dua atau tiga kali sehari. Hal ini bertujuan agar otot tidak kaget sehingga terhindar dari risiko cedera.
                                            </p>
                                            <p>
                                                Nah, itulah beberapa tips latihan fitnes untuk pemula. Selamat berlatih, ya!
                                            </p>
